package com.intel.rsd.nodecomposer.types;

public enum IpAddressType implements EnumeratedType {
    IP4("IPv4"),
    IP6("IPv6");

    private final String value;

    IpAddressType(String value) {
        this.value = value;
    }

    @Override
    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return getValue();
    }
}

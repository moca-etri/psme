package com.intel.rsd.nodecomposer.types;

public enum IpV4AddressOrigin implements EnumeratedType {
    STATIC("Static"),
    DHCP("DHCP"),
    BOOTP("BOOTP"),
    IPV4_LINK_LOCAL("IPv4LinkLocal");

    private final String value;

    IpV4AddressOrigin(String value) {
        this.value = value;
    }

    @Override
    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return getValue();
    }
}
